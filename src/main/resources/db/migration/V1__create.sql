CREATE TABLE  IF NOT EXISTS income(
 id BIGINT identity NOT NULL,
 amount decimal(10,2) NULL,
 date date NULL,
 year int null,
 month int null,
 name varchar(200),
 description VARCHAR(5000) NULL,
 operation_id BIGINT NULL,
 CONSTRAINT pk_income PRIMARY KEY (id)
 );

CREATE TABLE  IF NOT EXISTS ACCOUNT(
 ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
 NAME VARCHAR(200),
 BANK VARCHAR(200)
 );

CREATE TABLE IF NOT EXISTS OUTCOME(
 ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
 PRICE DECIMAL(10,2),COUNTER INTEGER,
 DATE DATE,
 YEAR INTEGER,
 MONTH INTEGER,
 NAME VARCHAR(200),
 CATHEGORY VARCHAR(200),
 DESCRIPTION VARCHAR(5000),
 OPERATION_ID INTEGER
 );

CREATE TABLE IF NOT EXISTS ACCOUNT_OPERATION(
 ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
 AMOUNT DECIMAL(10,2),COUNTER INTEGER,
 DATE DATE,
 YEAR INTEGER,
 MONTH INTEGER,
 NAME VARCHAR(200),
 DESCRIPTION VARCHAR(5000),
 ACCOUNT_ID INTEGER
 );

CREATE TABLE IF NOT EXISTS LOKATA(
 ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
 AMOUNT DECIMAL(10,2),
 PERCENT DECIMAL(10,2),
 DATE DATE,
 YEAR INTEGER,
 MONTH INTEGER,
 PERIOD VARCHAR(10),
 DESCRIPTION VARCHAR(200),
 BANK VARCHAR(200),
 ACTIVE BIT DEFAULT 1,
 PROLONGATED BIT DEFAULT 1,
 END_DATE DATE,
 INTEREST DECIMAL(10,2),
 INTEREST_NET DECIMAL(10,2),
 TOTAL_AMOUNT DECIMAL(10,2)
 );

CREATE TABLE IF NOT EXISTS ILLNESS(
 ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
 DATE DATE,
 YEAR INT,
 MONTH INT,
 NAME VARCHAR(200),
 WHO VARCHAR(200),
 DESCR VARCHAR(5000),
 FREE SMALLINT(3) DEFAULT 0
 );

CREATE TABLE IF NOT EXISTS CURE(
 ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
 MEDICINE VARCHAR(200),
 PRICE DECIMAL(10,2),
 DOSE VARCHAR(200),
 DESCR VARCHAR(5000),
 ID_IL INTEGER
 );
